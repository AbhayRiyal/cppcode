#include <iostream>
using namespace std;

int main(){
    int a;
    char b;
    float c;
    double d;
    bool e;
    cout<<"size of integer "<<sizeof(a)<<endl;
    cout<<"size of character "<<sizeof(b)<<endl;
    cout<<"size of float "<<sizeof(c)<<endl;
    cout<<"size of double "<<sizeof(d)<<endl;
    cout<<"size of bool "<<sizeof(e)<<endl;
 return 0;
}